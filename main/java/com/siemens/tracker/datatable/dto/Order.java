package com.siemens.tracker.datatable.dto;

/**
 * The Class Order.
 */
public class Order {
	
	/** The column. */
	private String column;
	
	/** The dir. */
	private String dir;
	
	/**
	 * Gets the column.
	 *
	 * @return the column
	 */
	public String getColumn() {
		return column;
	}
	
	/**
	 * Sets the column.
	 *
	 * @param column the new column
	 */
	public void setColumn(String column) {
		this.column = column;
	}
	
	/**
	 * Gets the dir.
	 *
	 * @return the dir
	 */
	public String getDir() {
		return dir;
	}
	
	/**
	 * Sets the dir.
	 *
	 * @param dir the new dir
	 */
	public void setDir(String dir) {
		this.dir = dir;
	}
	
	
}
